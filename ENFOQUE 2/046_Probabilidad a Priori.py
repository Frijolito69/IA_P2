# Número de pelotas
total_pelotas = 4
pelotas_rojas = 3
pelotas_azules = 1

# Cálculo de probabilidades a priori
probabilidad_roja = pelotas_rojas / total_pelotas
probabilidad_azul = pelotas_azules / total_pelotas

# Mostrar resultados
print(f"Probabilidad a priori de sacar una pelota roja: {probabilidad_roja:.2f}")
print(f"Probabilidad a priori de sacar una pelota azul: {probabilidad_azul:.2f}")
